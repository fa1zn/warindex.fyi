// Helper to get country by code
export function getCountryByCode(code: string): CountryMarket | undefined {
  return countryMarkets.find((c) => c.code === code);
}

// Get all countries sorted by GDP growth
export function getCountriesByGrowth(): CountryMarket[] {
  return [...countryMarkets].sort((a, b) => b.gdpGrowth - a.gdpGrowth);
}
